#! /usr/bin/env python3

"""
Basic Operators
"""

x = 5
y = 2
#Addition
print("Addition ", x + y)
#Substraction
print("Substraction ", x - y)
#Multiplication
print("Multiplication ", x * y)
#Division
print("Division ", x / y)
#Floor Division (rounds down the answer to the nearest whole number)
print("Floor Division ", x // y)
#Modulus (gives the reminder)
print("Modulus ", x % y)
#Exponent (5 to the power of 2 in this case)
print("Exponent", x ** y)
